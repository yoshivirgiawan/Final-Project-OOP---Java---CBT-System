package com.praktikum;

public class Choice {
    private String abjad;
    private String jawaban;

    public Choice(String abjad, String jawaban) {
        this.abjad = abjad;
        this.jawaban = jawaban;
    }

    public String getAbjad() {
        return abjad;
    }

    public String getJawaban() {
        return jawaban;
    }
}
